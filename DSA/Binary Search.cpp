//search for the target in the given sorted array
//Iterative code TC -> O(logn) base 2
int binarySearchITE(vector<int>& nums, int target) {
    int n = nums.size(); //size of the array
    int low = 0, high = n - 1;

    // Perform the steps:
    while (low <= high) {
        int mid = (low + high) / 2;
        if (nums[mid] == target) return mid;
        else if (target > nums[mid]) low = mid + 1;
        else high = mid - 1;
    }
    return -1;
}
//Recurcive code TC -> O(logn) base 2
int binarySearchREC(vector<int>& nums, int low, int high, int target) {

    if (low > high) return -1; //Base case.

    // Perform the steps:
    int mid = (low + high) / 2;
    if (nums[mid] == target) return mid;
    else if (target > nums[mid])
        return binarySearchREC(nums, mid + 1, high, target);
    return binarySearchREC(nums, low, mid - 1, target);
}
